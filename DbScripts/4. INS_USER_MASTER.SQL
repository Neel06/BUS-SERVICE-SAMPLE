
DELIMITER //
CREATE PROCEDURE INS_USER_MASTER(
	IN _EMAIL VARCHAR(255),
    IN _NAME VARCHAR(255),
    IN _PASSWORD VARCHAR(255),
    IN _BIRTH_DATE DATETIME,
    IN _ROLE_ID INT,
    IN _STUDENT_ID VARCHAR(255)
)
BEGIN
	INSERT INTO USER_MASTER (EMAIL,NAME,PASSWORD,BIRTH_DATE,ROLE_ID)
    VALUES(_EMAIL,_NAME,_PASSWORD,_BIRTH_DATE,_ROLE_ID);
    INSERT INTO STUDENT_MASTER (STUDENT_ID,EMAIL,NAME,BIRTH_DATE,IS_ACTIVE,CREATED_DATETIME) 
            VALUES(_STUDENT_ID,_EMAIL,_NAME,_BIRTH_DATE,TRUE,now());
END//
DELIMITER ;